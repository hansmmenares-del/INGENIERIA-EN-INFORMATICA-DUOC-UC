def area_rectangulo ():
    base_rec = float(input("ingresa el valor de la base:\n"))
    altura_rec = float(input("ingresa el valor de la altura:\n"))
    area_rec = base_rec*altura_rec
    print(f"El area del rectangulo de base {base_rec} y altura, {altura_rec} es de: {area_rec}")
    return()
area_rectangulo()